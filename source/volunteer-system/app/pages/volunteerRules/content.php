<h2>IMPORTANT</h2>
<ol>
    <li>Volunteers must be 12 years or older to volunteer in childcare</li>
    <li>Volunteers must be 16 years or older to volunteer in the Respite Care program.</li>
    <li>All minors must have a permission form signed by a parent/guardian prior to their first shift.</li>
    <li>All information obtained while volunteering on behalf of Kids On The Move will remain strictly confidential to ensure the protection and dignity of each participant, family member, and volunteer.</li>
    <li>Stay with your assigned child at all times</li>
    <li>Volunteers are NEVER permitted to be alone with a child.</li>
    <li>Do not take any child to the restroom alone; the KOTM Staff Member will supervise diapering/restroom needs of the children, as applicable.</li>
    <li>Keep staff informed of any needs of the children.</li>
    <li>Make sure the KOTM Staff is there when you release the child to his/her parent.</li>
    <li>Utah State Law requires citizens report any suspected abuse or neglect of a child. Report any suspected abuse to the KOTM Staff Member present.</li>
</ol>

<h2>Professional Conduct and Dress Code</h2>
<ol>
    <li>Volunteers must be of sound mind while providing service at KOTM. Staff members may ask volunteers to leave if they are not providing adequate or safe care.</li>
    <li>No smoking, drug use, or alcohol use on KOTM premises.</li>
    <li>No profanity.</li>
    <li>Clothing should be clean, in good repair, modest, and well fitting. Volunteers may be running around, bending over, etc. Comfortable shoes are recommended.</li>
    <li>Avoid jewelry that can be grabbed onto by the children or caught on things. Avoid extremes in clothing and cosmetics (such as costumes), as they may frighten children.</li>
</ol>

<h2>While With A Child</h2>
<ol>
    <li>Volunteers are NEVER permitted to be alone with a child.</li>
    <li>Do not take any child to the restroom alone; the KOTM Staff Member will supervise diapering/restroom needs of the children, as applicable.</li>
    <li>Stay with your assigned child at all times.</li>
    <li>Keep your child safe and clean.</li>
    <li>Use encouragement rather than competition.</li>
    <li>Please do not talk about children or their disabilities in their presence, even if they can’t talk or hear.</li>
    <li>Please do not use sarcasm or joke inappropriately with the children.</li>
    <li>Refrain from intimate displays of affection among volunteers.</li>
    <li>Avoid having older children sit in your lap. If a child insists on being held, discuss with the KOTM Staff Member.</li>
</ol>

<h2>Infection Control</h2>
<ol>
    <li>Wash hands often, especially after touching a nose or mouth, using the bathroom, before and after handling food, etc.</li>
    <li>Use gloves if touching body fluids (such as wiping a nose).</li>
    <li>Children learn by putting toys in their mouths, toys should be disinfected between children. Please put all toys that need to be disinfected in the sinks located in the rooms. The KOTM Staff Member will disinfect them.</li>
    <li>If you have any allergies to cleaning solutions, notify the KOTM Staff Member.</li>
    <li>Treat all human blood and other fluids as if known to be infectious, and do not touch them. Notify the KOTM Staff Member immediately.</li>
</ol>

<h2>Emergency Procedures</h2>
<ol>
    <li>In case of an emergency, utilize and keep with you the Kids On The Move Emergency Guide hanging in each room.</li>
    <li>Stay with your assigned child and help ensure his/her safety</li>
    <li>
        Medical Emergencies
        <ol>
            <li>The Staff will ask a volunteer to call 911. The volunteer will remove all other children from the room. Follow the directions of the KOTM Staff Member.</li>
            <li>Watch for: fever, pale color, pain, crying, moaning, groaning, lethargy, loss of consciousness, coughing, signals of allergies (runny nose, sneezing, itching, or swollen eyes), rashes, bruising or bleeding.</li>
        </ol>
    </li>
    <li>
        Evacuation Emergencies
        <ol>
            <li>Evacuate by the safest route. Meet on the lawn across the West Parking Lot.</li>
            <li>Clear your area as you leave. Leave with your child and take any other children and volunteers with you.</li>
            <li>Follow the directions of the KOTM Staff Member.</li>
        </ol>
    </li>
</ol>

<h2>Lifting Techniques—Only lift when absolutely necessary</h2>
<ol>
    <li>Lift with your legs, not with your back</li>
    <li>Keep close to your work</li>
    <li>Don’t twist while lifting</li>
    <li>Never jerk</li>
    <li>Let other muscles help</li>
    <li>Don’t overstrain</li>
    <li>Team lift if necessary</li>
</ol>